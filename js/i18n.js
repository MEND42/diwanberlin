let currentLang = localStorage.getItem('diwanLang') || 'de';

function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('diwanLang', lang);
  
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'fa' ? 'rtl' : 'ltr';
  
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang][key]) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = translations[lang][key];
      } else {
        el.innerHTML = translations[lang][key];
      }
    }
  });

  const toggleBtn = document.getElementById('langToggle');
  if (toggleBtn) {
    toggleBtn.innerHTML = lang === 'de' ? 'Persian' : 'Deutsch';
  }

  // Force re-render of menu if it exists
  if (typeof renderMenu === 'function') {
    renderMenu();
  }
  if (typeof renderPublicEvents === 'function') {
    renderPublicEvents();
  }
  if (typeof renderSiteContent === 'function') {
    renderSiteContent();
  }
}

document.addEventListener('DOMContentLoaded', () => {
  setLanguage(currentLang);
  
  const toggleBtn = document.getElementById('langToggle');
  if (toggleBtn) {
    toggleBtn.addEventListener('click', (e) => {
      e.preventDefault();
      setLanguage(currentLang === 'de' ? 'fa' : 'de');
    });
  }
});
