let currentLang = localStorage.getItem('diwanLang') || 'de';

const langLabels = {
  de: 'Deutsch',
  fa: 'فارسی',
  en: 'English'
};

function setLanguage(lang) {
  if (!translations[lang]) lang = 'de';
  currentLang = lang;
  localStorage.setItem('diwanLang', lang);
  
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'fa' ? 'rtl' : 'ltr';
  
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const value = translations[lang]?.[key] || translations.de?.[key];
    if (value) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = value.replace(/<[^>]*>/g, '');
      } else {
        el.innerHTML = value;
      }
    }
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    const value = translations[lang]?.[key] || translations.de?.[key];
    if (value) el.placeholder = value.replace(/<[^>]*>/g, '');
  });

  // Handle select option translations (data-i18n-opt)
  document.querySelectorAll('[data-i18n-opt]').forEach(el => {
    const key = el.getAttribute('data-i18n-opt');
    const value = translations[lang]?.[key] || translations.de?.[key];
    if (value) el.textContent = value;
  });

  // Handle aria-labels and other attributes
  document.querySelectorAll('[data-i18n-aria]').forEach(el => {
    const key = el.getAttribute('data-i18n-aria');
    const value = translations[lang]?.[key] || translations.de?.[key];
    if (value) el.setAttribute('aria-label', value);
  });

  // Update toggle button
  const toggleBtn = document.getElementById('langToggle');
  if (toggleBtn) {
    toggleBtn.innerHTML = langLabels[lang] || lang.toUpperCase();
  }

  // Update mobile toggle button
  const mobileToggle = document.getElementById('mobileLang');
  if (mobileToggle) {
    mobileToggle.textContent = langLabels[lang] || 'Language';
  }

  // Force re-render of menu if it exists
  if (typeof renderMenu === 'function') {
    renderMenu();
  }
  if (typeof renderPublicEvents === 'function') {
    renderPublicEvents();
  }
  if (typeof renderSiteContent === 'function') {
    renderSiteContent();
  }
}

function cycleLanguage() {
  const langs = ['de', 'fa', 'en'];
  const currentIdx = langs.indexOf(currentLang);
  const nextIdx = (currentIdx + 1) % langs.length;
  setLanguage(langs[nextIdx]);
}

document.addEventListener('DOMContentLoaded', () => {
  setLanguage(currentLang);
  
  // Desktop dropdown
  const toggleBtn = document.getElementById('langToggle');
  const langMenu = document.getElementById('langMenu');
  
  if (toggleBtn && langMenu) {
    toggleBtn.addEventListener('click', (e) => {
      e.preventDefault();
      e.stopPropagation();
      langMenu.classList.toggle('open');
    });
    
    langMenu.querySelectorAll('button[data-lang]').forEach(btn => {
      btn.addEventListener('click', (e) => {
        e.preventDefault();
        setLanguage(e.target.dataset.lang);
        langMenu.classList.remove('open');
      });
    });
  }
  
  // Close dropdown when clicking outside
  document.addEventListener('click', () => {
    if (langMenu) langMenu.classList.remove('open');
  });
  
  // Mobile language toggle
  const mobileToggle = document.getElementById('mobileLang');
  if (mobileToggle) {
    mobileToggle.addEventListener('click', (e) => {
      e.preventDefault();
      cycleLanguage();
      document.getElementById('hamburger')?.classList.remove('open');
      document.getElementById('mobile-overlay')?.classList.remove('open');
      document.body.classList.remove('nav-open');
      document.body.style.overflow = '';
    });
  }
});
